using System;

class CSharpDasar {
    static void Main(string[] args) {

        //Increment dan Decrement
        int a = 3;
        System.Console.WriteLine(a); //Output : 3
        System.Console.WriteLine(++a);//Output : 4
        System.Console.WriteLine(a);//Output : 4
        //int a = 3;
        System.Console.WriteLine(a);//Output : 3
        System.Console.WriteLine(++a);//Output : 3
        System.Console.WriteLine(a);//Output : 4
        //int a = 3;
        System.Console.WriteLine(a);//Output : 3
        System.Console.WriteLine(--a);//Output : 2
        System.Console.WriteLine(a);//Output : 2
        //int a = 3;
        System.Console.WriteLine(a); //Output : 3
        System.Console.WriteLine(a--);//Output : 3
        System.Console.WriteLine(a);//Output : 2

    }
}